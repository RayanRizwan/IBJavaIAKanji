    private Teacher createTeacher(String username, String password, String passphrase, String language, String ClassCode){
        // Create filler classes that the Teacher can add people to
        ArrayList<Student> Class = new ArrayList<>();
        ArrayList<String> Curriculum = new ArrayList<>();
        
        return new Teacher(Class, Curriculum, username, password, passphrase, language, ClassCode);
    }
    
   private Student createStudent(String level, String username, String password, String passphrase, String language, String ClassCode){
       // Same concept as with Teacher, filler classes that the Student can add words to later
       ArrayList<Kanji> Kanji = new ArrayList<>();
       ArrayList<Word> VocabList = new ArrayList<>();
        
       Teacher x = main.findClass(ClassCode);
       return new Student(level, Kanji, VocabList, x, username, password, passphrase, language);
   }
   
   private void ExitSignUp(Sign_Up l){
       if (main.currentStudent != null){
           // Created file is tited off of the users username
           Serialiser.serialiseKanjiList(main.currentStudent.Kanji, (main.currentStudent.username + "_kanji_list.ser"));
           main.SerialiseStudents(main.Students, "All_Students.ser");
           main.SerialiseTeachers(main.Teacher, "All_Teachers.ser");
       }
       l.setVisible(false);
   }

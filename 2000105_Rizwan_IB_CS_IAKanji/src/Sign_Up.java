    private void setKanjiList(Student l) {
        // find the CSV file with all the Kanji
        String csvFile = "heisig-kanjis.csv";
        String delimiter = ",";
        // InputStream here simply opens the stream
        try (InputStream is = getClass().getClassLoader().getResourceAsStream(csvFile)) {
            // need to ensure that is isn't null and the csvFile is read
            if (is == null) {
                throw new FileNotFoundException("heisig-kanjis not found in classpath");
            }
            // Bufferring simply allows for incredible efficiency, 
            // reading thousands of lines at once
            try (BufferedReader br = new BufferedReader(new InputStreamReader(is))){
                String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(delimiter);

                // Makes sure that there is not too much data that errors are given
                if (data.length < 10) continue;

                // Create a new Kanji object
                Kanji kanji = new Kanji();
                kanji.setIndex(Integer.parseInt(data[2])); // Data[1] should always be an integer given the contents of the CSV
                kanji.setKanji(data[0]);
                kanji.setMeaning(data[3]);
                kanji.setKunyomi(data[7]);
                kanji.setOnyomi(data[6]);

                // Determine which list to add based on level
                // data[9] is basically a level set by the Japanese government
                // The reason it is not a variable is because this is its only instance
                if (l.level.equals("Joyo")) {
                    l.Kanji.add(kanji);
                } else if (l.level.equals("HL") && (data[9].equals("N5") ||
                        data[9].equals("N4") || data[9].equals("N3"))) {
                    l.Kanji.add(kanji);
                } else if (l.level.equals("SL") && (data[9].equals("N5") ||
                        data[9].equals("N4"))) {
                    l.Kanji.add(kanji);
                }
            }
            }
            
        } catch (IOException e) {
            // Catches like this were originally debugging tools that I frankly
            // Found from StackOverflow, but have been kept to ensure nothing goes
            // wrong
            e.printStackTrace();
        } catch (NumberFormatException e) {
            System.err.println("Error parsing integer from CSV: " + e.getMessage());
        }
        
        
    }



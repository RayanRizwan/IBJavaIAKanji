    private void ButtonSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSignUpActionPerformed
        // TODO add your handling code here:
        // required to make the constructor in which the new user is held

        username = TextFieldUsername.getText();
        password = TextFieldPassword.getText();
        passphrase = TextFieldPassphrase.getText();
        language = ComboBoxLanguage.getSelectedItem().toString();
        // Originally each class was going to have a classcode through which
        // students could join, but this was scrapped do to a lack of necessity
  
        classCode = "000AAA";

        
        if (RadioButtonStudent.isSelected()){
            // checks if the new user is a teacher or a student
            student = true;
        }
        else{
            teacher = true;
        }
        
        // Set the students level to what he has indicated in the Sign Up form
        if (RadioButtonSL.isSelected()){
            level = "SL";
        }
        else if (RadioButtonHL.isSelected()){
            level = "HL";
        }
        else{
            level = "Joyo";
        }
        
        // Checks whether user is a student or teacher before creating new user
        if (teacher){
            Teacher x = createTeacher(username, password, passphrase, language, classCode);
            main.addTeacher(x);
            main.currentTeacher = x;
            main.currentStudent = null;
        }
        else if (student){
            Student x = createStudent(level, username, password, passphrase, language, classCode);
            main.addStudents(x);
            // adds student to the teacher's class
            (x.teacher).addStudentstoClass(x);
            main.currentStudent = x;
            // ensures there is only one user at a time, regardless of 
            // whether they are a student or teacher
            main.currentTeacher = null;
            setKanjiList(x);
        }
        
        // Changes language depending on what was set
        main.changeLocale(language);
        
        
        ExitSignUp(this);
        
        
    }//GEN-LAST:event_ButtonSignUpActionPerformed

